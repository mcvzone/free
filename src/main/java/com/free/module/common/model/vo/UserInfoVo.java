package com.free.module.common.model.vo;

public class UserInfoVo {
	private String id;
	private String name;
	private String age;
	private String sex;
	private String location;
	private String connect_ip;
	private String power_level;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getAge() {
		return age;
	}
	public void setAge(String age) {
		this.age = age;
	}
	public String getSex() {
		return sex;
	}
	public void setSex(String sex) {
		this.sex = sex;
	}
	public String getLocation() {
		return location;
	}
	public void setLocation(String location) {
		this.location = location;
	}
	public String getConnect_ip() {
		return connect_ip;
	}
	public void setConnect_ip(String connect_ip) {
		this.connect_ip = connect_ip;
	}
	public String getPower_level() {
		return power_level;
	}
	public void setPower_level(String power_level) {
		this.power_level = power_level;
	}
	
}
